1. J2EE (Java 2 Platform, Enterprise Edition)

  - J2EE (Java 2 Platform, Enterprise Edition) is a specification defined by Sun/Oracle for building large-scale, 
        distributed, and secure enterprise applications.
  - It provides a standardized set of server-side components like Servlets, JSP, EJB, JMS, JTA, and JDBC to solve common enterprise problems 
        such as transaction management, messaging, scalability, and security.
  - Spring and Spring Boot later emerged as lightweight alternatives that follow the same enterprise principles but simplify development 
        by reducing the complexity of classic J2EE.
